public class Person {
    // Attributes
    String role;
    String firstName;
    String surname;
    String email;
    String address;

    // Methods

    // Constructor
    public Person (String role, String firstName, String surname, String email, String address){
        this.role = role;
        this.firstName = firstName;
        this.surname = surname;
        this.email = email;
        this.address = address;
    }
}
